    private void loginJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJButtonActionPerformed
        // TODO add your handling code here:

        userProcessContainer.removeAll();

        String userName = userNameJTextField.getText();
        
        if(userName.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter User Name");
            return;
        }
    
        char[] pass = passwordJPasswordField.getPassword();
        String password = String.valueOf(pass);
        
        if(password.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Password");
            return;
        }
                
        UserAccount ua = business.authenticateUser(userName, password);

        if (ua != null) {
            
            JPanel newJPanel = ua.getRole().returnWorkArea().createView(userProcessContainer, ua).returnPanel();
            userNameJTextField.setEditable(false);
            passwordJPasswordField.setEditable(false);
            loginJButton.setEnabled(false);
            userProcessContainer.add("New Panel", newJPanel);
            CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
            cardLayout.next(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(null, "Invalid Username Or Password Or Enterprise");
        }
    }//GEN-LAST:event_loginJButtonActionPerformed

