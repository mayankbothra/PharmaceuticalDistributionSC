    private void refreshPatientHistoryList(Patient patient) {
        
        int rowCount = prescriptionListJTable.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            ((DefaultTableModel) prescriptionListJTable.getModel()).removeRow(i);
        }
        
        for (VisitRecord vr : patient.getPatientHistory().getVisitRecordList()) {
            for (InventoryItem ii : vr.getInventory().getInventoryItemList()) {
                Object row[] = new Object[3];
                row[0] = ii.getDrug();
                row[1] = ii;
                row[2] = vr.getDateOfVisit();
//                TableColumn uniqueId = prescriptionListJTable.getColumnModel().getColumn(3);
//                JComboBox uniqueIDJComboBox = new JComboBox();
//                for (MedicinePackage mp : ii.getMedicinePackageList()) {
//                    uniqueIDJComboBox.addItem(mp);
//                }
//                uniqueId.setCellEditor(new DefaultCellEditor(uniqueIDJComboBox));
                ((DefaultTableModel) prescriptionListJTable.getModel()).addRow(row);
            }
        }
        
    }

