    private void selectSuspectJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectSuspectJButtonActionPerformed
        // TODO add your handling code here:
        selectedRow = prescriptionListJTable.getSelectedRow();
        
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select Drug To Add It To The Suspect Package List");
            return;
        }
        
        InventoryItem ii= (InventoryItem)prescriptionListJTable.getValueAt(selectedRow, 1);
        
        jComboBox1.removeAllItems();
        for(MedicinePackage mp : ii.getMedicinePackageList()) {
            jComboBox1.addItem(mp);
        }
        
        selectPackageJButton.setEnabled(true);
        
        
        
        
        
        
    }//GEN-LAST:event_selectSuspectJButtonActionPerformed

    private void selectPackageJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectPackageJButtonActionPerformed
        // TODO add your handling code here:
        MedicinePackage mp = (MedicinePackage) jComboBox1.getSelectedItem();

       tempSuspectPackage.setMedicinePackage(mp);
        
        
        String detail = JOptionPane.showInputDialog(null, "Enter Suspect Package Details");
        tempSuspectPackage.setDetails(detail);
        
        
        Object row[] = new Object[3];
        row[0] = tempSuspectPackage.getMedicinePackage();
        row[1] = tempSuspectPackage.getMedicinePackage().getBatch().getDrug().getDrugName();
        row[2] = tempSuspectPackage.getDetails();
        
        ((DefaultTableModel) suspectDrugsListJTable.getModel()).addRow(row);
        
        selectSuspectJButton.setEnabled(false);
        selectPackageJButton.setEnabled(false);
    }//GEN-LAST:event_selectPackageJButtonActionPerformed

    private void goJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_goJButtonActionPerformed
        // TODO add your handling code here:
        String patientName = patientNameJTextField.getText();
        
        ArrayList<Enterprise> entList = Business.Business.getInstance().getEnterpriseList(Enterprise.Type.HOSPITAL);
        
        for(Enterprise e : entList) {
            Hospital h = (Hospital)e;
            
            for(Patient p : h.getPatientDirectory().getPatientList()) {
                if(p.getPerson().getFirstName().equalsIgnoreCase(patientName)) {
                    patient = p;
                }
            }
            
        }
        
        refreshPatientHistoryList(patient);
    }//GEN-LAST:event_goJButtonActionPerformed

