    private void viewDetailsJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = inventoryJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select A Row To View Details");
            return;
        }

        InventoryItem ii = (InventoryItem)inventoryJTable.getValueAt(selectedRow, 3);
        ViewInventoryItemDetailJPanel viewInventoryItemDetailJPanel = new ViewInventoryItemDetailJPanel(userProcessContainer, selectedEnterprise, userAccount, selectedNetwork, ii);
        userProcessContainer.add("ViewInventoryDetails", viewInventoryItemDetailJPanel);
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        cardLayout.next(userProcessContainer);
    }//GEN-LAST:event_viewDetailsJButtonActionPerformed

