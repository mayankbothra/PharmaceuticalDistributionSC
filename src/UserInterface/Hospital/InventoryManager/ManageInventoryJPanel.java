    private void refreshInventory() {
        
        
        int rowCount = inventoryJTable.getRowCount();

        for (int i = rowCount - 1; i >= 0; i--) {
            ((DefaultTableModel) inventoryJTable.getModel()).removeRow(i);
        }

        for (InventoryItem ii : selectedEnterprise.getInventory().getInventoryItemList()) {
                
                Object[] row = new Object[4];
                row[0] = ii.getDrug().getDrugId();
                row[1] = ii.getDrug().getDrugName();
                row[2] = ii.getDrug().getManufacturerName();
                row[3] = ii;
                ((DefaultTableModel) inventoryJTable.getModel()).addRow(row);
//            getTableCellRendererComponent(inventoryJTable,batch , true, true, i, 3);
            
        }

    }

