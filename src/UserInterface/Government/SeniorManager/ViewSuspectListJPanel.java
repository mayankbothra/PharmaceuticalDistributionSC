    private void declareIllegitimateJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_declareIllegitimateJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = suspectDrugJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select Package");
            return;
        }

        ArrayList<MedicinePackage> tempList = new ArrayList<>();

        MedicinePackage mp = (MedicinePackage) suspectDrugJTable.getValueAt(selectedRow, 0);
        Drug drug = mp.getBatch().getDrug();

        selectedEnterprise.getIllegitimateDrugList().add(drug);

        for (MedicinePackage m : selectedEnterprise.getSuspectPackageList()) {
            if (m.getBatch().getDrug() == drug) {
                tempList.add(m);
            }
        }

        for (MedicinePackage m : tempList) {
            selectedEnterprise.getSuspectPackageList().remove(m);
        }
        
        refreshSuspectList();

        JOptionPane.showMessageDialog(null, "Selected Drug Added To Illegitimate Drug List");

    }//GEN-LAST:event_declareIllegitimateJButtonActionPerformed

    private void declareCleanJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_declareCleanJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = suspectDrugJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select Package");
            return;
        }

        MedicinePackage mp = (MedicinePackage) suspectDrugJTable.getValueAt(selectedRow, 0);
        
        selectedEnterprise.getSuspectPackageList().remove(mp);
        JOptionPane.showMessageDialog(null, "Package Declared Clear And Removed From Suspect List");
        
        refreshSuspectList();
    }//GEN-LAST:event_declareCleanJButtonActionPerformed

    private void sendAlertJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendAlertJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = suspectDrugJTable.getSelectedRow();
        
        MedicinePackage mp = (MedicinePackage)suspectDrugJTable.getValueAt(selectedRow, 0);
        
        ArrayList<Enterprise> entList = Business.Business.getInstance().getEnterpriseList(Enterprise.Type.MANUFACTURER);
        
        for(Enterprise e : entList) {
            Manufacturer m = (Manufacturer)e;
            if(m.getEnterpriseName().equals(mp.getBatch().getDrug().getManufacturerName())) {
                m.setFlag(false);
                m.setAlertMessage("'" + mp.getUniqueID() + "' Package Is Declared Suspect");
            }
        }
        
        for(Network n : Business.Business.getInstance().getNetworkDirectory().getNetworkList()) {
            for(Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
                for(Enterprise ee : entList) {
                    if(!(e==ee)) {
                        e.setFlag(false);
                        e.setAlertMessage("'" + mp.getUniqueID() + "' Package Is Declared Suspect. It Belongs To Manufacturer '" + mp.getBatch().getDrug().getManufacturerName() + "' Manufacturer");
                    }
                }
                
            }
        }
        
    }//GEN-LAST:event_sendAlertJButtonActionPerformed
