    public void refreshSuspectList() {

        int rowCount = suspectDrugJTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel) suspectDrugJTable.getModel();

        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }


        for (MedicinePackage mp : selectedEnterprise.getSuspectPackageList()) {
            Object row[] = new Object[4];
            row[0] = mp;
            row[1] = mp.getBatch().getDrug().getDrugName();
            row[2] = mp.getBatch().getBatchID();
            row[3] = mp.getBatch().getDrug().getManufacturerName();

            model.addRow(row);

        }

    }

