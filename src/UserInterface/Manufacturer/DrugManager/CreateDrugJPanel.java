    private void sendForApprovalJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendForApprovalJButtonActionPerformed
        // TODO add your handling code here:
        Government govt = (Government) selectedNetwork.getEnterpriseDirectory().getSelectedEnterprise(Enterprise.Type.GOVERNMENT);
        request = (DrugCreationWorkRequest) govt.getGovernmentDrugLicensingOrganization().getWorkQueue().newWorkRequest(WorkRequest.Type.DrugCreationRequest);

        request.setSender(userAccount);
        SimpleDateFormat sdf = new SimpleDateFormat("MM dd yy HH:mm:ss");
        String currentDate = sdf.format(new Date());
        request.setRequestDate(currentDate);
        request.setDrug(drug);
        request.setDecision(DrugCreationWorkRequest.status.PENDING.toString());

        Organization o = selectedEnterprise.getMyOrganization(userAccount);
        o.getSentWorkQueue().addRequest(request);

        JOptionPane.showMessageDialog(null, "Drug Send To FDA for Approval");
        userProcessContainer.remove(this);
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        cardLayout.previous(userProcessContainer);
    }//GEN-LAST:event_sendForApprovalJButtonActionPerformed
