    private void createJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createJButtonActionPerformed
        // TODO add your handling code here:

        if (drugNameJTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Drug Name");
            return;
        }

        if (fakeEnt) {
            drug = selectedEnterprise.getDrugCatalog().newDrug();
            selectedEnterprise.getDrugCatalog().addDrug(drug);
            drug.setDrugName(drugNameJTextField.getText());
            drug.setManufacturerName(selectedEnterprise.toString());
            int count = Drug.getCount();
            count++;
            Drug.setCount(count);
            drug.setDrugId(count);

            drugNameJTextField.setEditable(false);
            createJButton.setEnabled(false);
            clearJButton.setEnabled(false);
            
            JOptionPane.showMessageDialog(null, "Fake Drug Created");
        } else {
            drug = selectedEnterprise.getDrugCatalog().newDrug();
            drug.setDrugName(drugNameJTextField.getText());
            drug.setManufacturerName(selectedEnterprise.toString());

            drugNameJTextField.setEditable(false);
            createJButton.setEnabled(false);
            clearJButton.setEnabled(false);
            sendForApprovalJButton.setEnabled(true);
        }

    }//GEN-LAST:event_createJButtonActionPerformed

