    private void refreshDrugCatalog() {
        int rowCount = drugCatalogJTable.getRowCount();
        
        for(int i = rowCount - 1; i >= 0; i--) {
            ((DefaultTableModel)drugCatalogJTable.getModel()).removeRow(i);
        }
        
        for(Drug drug : selectedEnterprise.getDrugCatalog().getDrugList()) {
            Object[] row = new Object[2];
            row[0] = drug.getDrugId();
            row[1] = drug;
            
            ((DefaultTableModel)drugCatalogJTable.getModel()).addRow(row);
        }
    }
    
    private void refreshInventory(Drug drug) {
        
        Drug d = drug;
        
        int rowCount = inventoryJTable.getRowCount();

        for (int i = rowCount - 1; i >= 0; i--) {
            ((DefaultTableModel) inventoryJTable.getModel()).removeRow(i);
        }

        for (InventoryItem ii : selectedEnterprise.getInventory().getInventoryItemList()) {
            
            if(ii.getDrug() == d) {
                Object[] row = new Object[3];
                row[0] = ii.getDrug().getDrugId();
                row[1] = ii.getDrug().getDrugName();
                row[2] = ii;
                ((DefaultTableModel) inventoryJTable.getModel()).addRow(row);
            
            }
//            getTableCellRendererComponent(inventoryJTable,batch , true, true, i, 3);
        }
}

