    private void viewDetailsJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = inventoryJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select A Row To View Details");
            return;
        }
        
        InventoryItem ii = (InventoryItem)inventoryJTable.getValueAt(selectedRow, 2);
        ViewInventoryItemDetailJPanel viewInventoryItemDetailJPanel = new ViewInventoryItemDetailJPanel(userProcessContainer, selectedEnterprise, userAccount, selectedNetwork, ii);
        userProcessContainer.add("ViewInventoryDetails", viewInventoryItemDetailJPanel);
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        cardLayout.next(userProcessContainer);
    }//GEN-LAST:event_viewDetailsJButtonActionPerformed

    private void viewInInventoryJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewInInventoryJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = drugCatalogJTable.getSelectedRow();
        
        if(selectedRow < 0) {
           JOptionPane.showMessageDialog(null, "Select A Drug To View Inventory Details");
            return; 
        }
        
        Drug drug = (Drug)drugCatalogJTable.getValueAt(selectedRow, 1);
        refreshInventory(drug);
        
    }//GEN-LAST:event_viewInInventoryJButtonActionPerformed

    private void orderInventoryJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderInventoryJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = drugCatalogJTable.getSelectedRow();
        
        if(selectedRow < 0) {
           JOptionPane.showMessageDialog(null, "Select A Drug To Place Batch Creation Order");
            return; 
        }
        
        Drug drug = (Drug)drugCatalogJTable.getValueAt(selectedRow, 1);
        
        request = (CreateBatchWorkRequest)selectedEnterprise.getManufacturingOrganization().getWorkQueue().newWorkRequest(WorkRequest.Type.CreateBatchRequest);
        request.setDrug(drug);
        SimpleDateFormat sdf = new SimpleDateFormat("MM dd yy HH:mm:ss");
        String currentDate = sdf.format(new Date());
        request.setRequestDate(currentDate);
        request.setSender(userAccount);
        request.setStatus(WorkRequest.Status.CreateBatch);
        
        JOptionPane.showMessageDialog(null, "Batch Creation Order Placed To Manufacturing Department");
        refreshDrugCatalog();
    }//GEN-LAST:event_orderInventoryJButtonActionPerformed

