    public EnterpriseAdminWorkAreaForLicenseJPanel(JPanel userProcessContainer, UserAccount userAccount) {
        initComponents();
        this.userProcessContainer = userProcessContainer;
        this.userAccount = userAccount;
        business = Business.getInstance();
        
        selectedNetwork = business.getNetworkDirectory().getMyNetwork(userAccount);
        selectedEnterprise = selectedNetwork.getEnterpriseDirectory().getMyEnterprise(userAccount);
        
        networkNameJLabel.setText(selectedNetwork.getNetworkName());
        enterpriseTypeJLabel.setText(" --  " + selectedEnterprise.getEnterpriseName());        
        nameJLabel.setText(userAccount.getEmployee().getPerson().getFirstName() + " " + userAccount.getEmployee().getPerson().getLastName() + " -- ");
        
        if(selectedEnterprise.getLicenseCount() == 0) {
            applyForLicenseJButton.setEnabled(true);
        }
        else if(selectedEnterprise.getLicenseCount() == -1) {
            JOptionPane.showMessageDialog(null, "License Request Rejected. Resend an Updated Request");
            selectedEnterprise.setLicenseCount(0);
            applyForLicenseJButton.setEnabled(true);
        }
        else if(selectedEnterprise.getLicenseCount() == -2) {
            JOptionPane.showMessageDialog(null, "License Expired. Reapply For License");
            selectedEnterprise.setLicenseCount(0);
            applyForLicenseJButton.setEnabled(true);

        }
        else {
            JOptionPane.showMessageDialog(null, "License Approval Request Already Sent. Wait For Approval From Law Agency");
            userProcessContainer.remove(this);
            CardLayout cardLayout = (CardLayout)userProcessContainer.getLayout();
            cardLayout.previous(userProcessContainer);
        }
    }

