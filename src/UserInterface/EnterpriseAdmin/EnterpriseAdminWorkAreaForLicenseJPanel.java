    private void applyForLicenseJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyForLicenseJButtonActionPerformed
        // TODO add your handling code here:
        ApplyForLicenseWorkRequest request;
        
        Government govt = (Government)selectedNetwork.getEnterpriseDirectory().getSelectedEnterprise(Enterprise.Type.GOVERNMENT);
        
        if(selectedEnterprise.getEnterpriseType().equals(Enterprise.Type.MANUFACTURER.toString())) {
            request = (ApplyForLicenseWorkRequest)govt.getGovernmentFederalLicenseManagementOrganization().getWorkQueue().newWorkRequest(WorkRequest.Type.ApplyForLicenseRequest); 
            JOptionPane.showMessageDialog(null, "Application For License Approval Sent To Federal Licensing Organization");
        }
        
        else {
            request = (ApplyForLicenseWorkRequest)govt.getGovernmentStateLevelLicensingOrganization().getWorkQueue().newWorkRequest(WorkRequest.Type.ApplyForLicenseRequest);
            JOptionPane.showMessageDialog(null, "Application For License Approval Sent To State Level Licensing Organization");
        }

        
        SimpleDateFormat sdf = new SimpleDateFormat("MM dd yy HH:mm:ss");
        String currentDate = sdf.format(new Date());
        request.setRequestDate(currentDate);
        request.setSender(userAccount);
        request.setStatus(WorkRequest.Status.ForApproval);
        request.setDecision(ApplyForLicenseWorkRequest.status.PENDING.toString());
        
        selectedEnterprise.setLicenseCount(selectedEnterprise.getLicenseCount()+1);
        applyForLicenseJButton.setEnabled(false);
        
                     
    }//GEN-LAST:event_applyForLicenseJButtonActionPerformed

